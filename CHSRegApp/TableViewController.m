- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    
    TableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"tblCell"];
    if (cell == nil) {
        cell = [[TableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:@"tblCell"];
    
    }
    
    // Configure the cell...

    cell.locationLabel.text = self.collectionCellArray[indexPath.row];
    cell.distanceLabel.text = @"55.5 Miles";
    cell.waitTimeLabel.text = @"Wait: 1 hour 24 min";

    

    
    return cell;
}

